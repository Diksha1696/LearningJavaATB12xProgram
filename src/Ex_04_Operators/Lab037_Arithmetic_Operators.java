package Ex_04_Operators;

public class Lab037_Arithmetic_Operators {
    public static void main(String[] args) {
        //Arithmetic Operators
        //+ addition
        // - subtraction
        // * multiplication
        // / Division
        // % Modulo

        int a = 10;
        int b = 4;
        float c = 3.0f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c); //output will be in decimal
    }
}
