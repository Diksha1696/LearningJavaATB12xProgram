package Ex_04_Operators;

public class Lab042_Interview_Concat_Plus {
    public static void main(String[] args) {
        // concatenation
        String s1 = "Diksha";
        String s2 = "Kumari";
        System.out.println(s1+s2);  // output= DikshaKumari

        int a = 10;
        int b = 20;
        System.out.println(a+b); // output = 30
    }
}
